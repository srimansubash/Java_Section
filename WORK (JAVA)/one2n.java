import java.util.Scanner;
public class one2n 
{
    public static void main(String args[]) 
    {
        Scanner sc=new Scanner(System.in);
        int n;
        System.out.println("enter the value:");
        n=sc.nextInt();
        for (int i=1;i<=n;i++){
            System.out.println(i);
        }
    }   
}
